package model;

/**
 *
 * @author user
 */
public class Location {
    private int id;
    private String name;
    private int countryId;

    public Location() {
    }

    public Location(int id, String name, int countryId) {
        this.id = id;
        this.name = name;
        this.countryId = countryId;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getCountryId() {
        return countryId;
    }

    public void setCountryId(int countryId) {
        this.countryId = countryId;
    }

    @Override
    public String toString() {
        return "Location{" + "id=" + id + ", name=" + name + ", countryId=" + countryId + '}';
    }
    
    
}
